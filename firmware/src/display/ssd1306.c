static void ssd1306_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR, buf, 2, false);
}

void ssd1306_init(void) {
    i2c_init(SSD1306_I2C_PORT, SSD1306_I2C_SPEED);
    gpio_set_function(SSD1306_SDA_PIN, GPIO_FUNC_I2C);
    gpio_set_function(SSD1306_SCL_PIN, GPIO_FUNC_I2C);
    gpio_pull_up(SSD1306_SDA_PIN);
    gpio_pull_up(SSD1306_SCL_PIN);

    sleep_ms(10);

    ssd1306_cmd(0xAE); // display off
    ssd1306_cmd(0xD5); ssd1306_cmd(0x80); // clock divide
    ssd1306_cmd(0xA8); ssd1306_cmd(0x3F); // multiplex ratio 64
    ssd1306_cmd(0xD3); ssd1306_cmd(0x00); // display offset 0
    ssd1306_cmd(0x40);                     // start line 0
    ssd1306_cmd(0x8D); ssd1306_cmd(0x14); // charge pump on
    ssd1306_cmd(0x20); ssd1306_cmd(0x00); // horizontal addressing
    ssd1306_cmd(0xA1);                     // segment remap
    ssd1306_cmd(0xC8);                     // COM scan direction remapped
    ssd1306_cmd(0xDA); ssd1306_cmd(0x12); // COM pins
    ssd1306_cmd(0x81); ssd1306_cmd(0xCF); // contrast
    ssd1306_cmd(0xD9); ssd1306_cmd(0xF1); // pre-charge
    ssd1306_cmd(0xDB); ssd1306_cmd(0x40); // VCOMH deselect
    ssd1306_cmd(0xA4);                     // resume RAM display
    ssd1306_cmd(0xA6);                     // normal (non-inverted)
    ssd1306_cmd(0xAF);                     // display on

    ssd1306_clear();
    ssd1306_display();
}

void ssd1306_clear(void) {
    memset(g_framebuffer, 0, sizeof(g_framebuffer));
}

void ssd1306_display(void) {
    ssd1306_cmd(0x21); ssd1306_cmd(0); ssd1306_cmd(127); // column 0-127
    ssd1306_cmd(0x22); ssd1306_cmd(0); ssd1306_cmd(7);   // page 0-7

    // Send all 1024 bytes with 0x40 data prefix in one burst
    uint8_t buf[SSD1306_WIDTH * SSD1306_PAGES + 1];
    buf[0] = 0x40;
    for (int p = 0; p < SSD1306_PAGES; p++) {
        memcpy(&buf[1 + p * SSD1306_WIDTH], g_framebuffer[p], SSD1306_WIDTH);
    }
    i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR, buf, sizeof(buf), false);
}

void ssd1306_display_on(void) {
    ssd1306_cmd(0x8D); ssd1306_cmd(0x14);
    ssd1306_cmd(0xAF);
}

void ssd1306_display_off(void) {
    ssd1306_cmd(0xAE);
    ssd1306_cmd(0x8D); ssd1306_cmd(0x10); // charge pump off
}

void ssd1306_set_contrast(uint8_t contrast) {
    ssd1306_cmd(0x81);
    ssd1306_cmd(contrast);
}

void ssd1306_draw_pixel(int x, int y, bool on) {
    if (x < 0 || x >= SSD1306_WIDTH || y < 0 || y >= SSD1306_HEIGHT) return;
    int page = y / 8;
    int bit  = y % 8;
    if (on)
        g_framebuffer[page][x] |=  (1 << bit);
    else
        g_framebuffer[page][x] &= ~(1 << bit);
}

void ssd1306_draw_hline(int x, int y, int w) {
    for (int i = 0; i < w; i++) ssd1306_draw_pixel(x + i, y, true);
}

void ssd1306_draw_vline(int x, int y, int h) {
    for (int i = 0; i < h; i++) ssd1306_draw_pixel(x, y + i, true);
}

void ssd1306_draw_rect(int x, int y, int w, int h) {
    ssd1306_draw_hline(x,         y,         w);
    ssd1306_draw_hline(x,         y + h - 1, w);
    ssd1306_draw_vline(x,         y,         h);
    ssd1306_draw_vline(x + w - 1, y,         h);
}

void ssd1306_fill_rect(int x, int y, int w, int h) {
    for (int row = y; row < y + h; row++) {
        ssd1306_draw_hline(x, row, w);
    }
}

void ssd1306_invert_rect(int x, int y, int w, int h) {
    for (int px = x; px < x + w; px++) {
        for (int py = y; py < y + h; py++) {
            if (px < 0 || px >= SSD1306_WIDTH || py < 0 || py >= SSD1306_HEIGHT) continue;
            int page = py / 8;
            int bit  = py % 8;
            g_framebuffer[page][px] ^= (1 << bit);
        }
    }
}

void ssd1306_draw_bitmap(int x, int y, const uint8_t *bmp, int w, int h) {
    for (int col = 0; col < w; col++) {
        for (int row = 0; row < h; row++) {
            if (bmp[col] & (1 << row)) {
                ssd1306_draw_pixel(x + col, y + row, true);
            }
        }
    }
}

void ssd1306_draw_char(int x, int y, char c) {
    if (c < 0x20 || c > 0x7E) c = '?';
    const uint8_t *glyph = FONT5X7[c - 0x20];
    for (int col = 0; col < 5; col++) {
        for (int row = 0; row < 7; row++) {
            if (glyph[col] & (1 << row)) {
                ssd1306_draw_pixel(x + col, y + row, true);
            }
        }
    }
}

void ssd1306_draw_string(int x, int y, const char *s) {
    int cx = x;
    while (*s) {
        ssd1306_draw_char(cx, y, *s++);
        cx += 6;
    }
}

int ssd1306_string_width(const char *s) {
    int len = 0;
    while (*s++) len++;
    return len * 6 - 1; // 5px glyph + 1px spacing, no trailing space
}

void ssd1306_draw_string_centered(int y, const char *s) {
    int w = ssd1306_string_width(s);
    int x = (SSD1306_WIDTH - w) / 2;
    if (x < 0) x = 0;
    ssd1306_draw_string(x, y, s);
}
